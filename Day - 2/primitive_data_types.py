# Learn about the primitive data types in Python
# Strings
# Integers
# Floats
# Booleans

# Subscripting
print("Hello"[4])  # prints the o in Hello

# String
print("123" + "345")

# Integer = whole number
print(123 + 345)

# Large integers. Ex: 123,456,789
print(123_456_789)

# Float = floating point number. Ex: 3.1459
print(3.1459)

# Boolean = True or False
print(True)
print(False)