# Flooring a number

# using bmi coding exercise information
bmi = 84 / 1.65 ** 2
print(bmi)  # prints bmi
print(int(bmi))  # converts bmi to integer
print(round(bmi))  # rounds bmi depending on decimal point
print(round(bmi, 2))  # rounds 2 decimal points

print("\n")

