# Learn to use the basic mathematical operators: +, -, *, /, and **
# PEMDAS: Parenthesis, Exponents, Multiplication, Division, Addition, Subtraction
print("My age: " + str(12))

print(123 + 123)  # addition
print(7 - 3)  # subtraction
print(3 * 2)  # multiplication
print(6 / 3)  # division
print(5 // 3)  # no remainder division 1.6666 becomes 1
print(12 ** 12)  # exponent

# PAUSE 1: What is the output of the code below?
