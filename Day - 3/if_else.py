# Learn to use conditions in Python: if/else

print("Welcome to the rollercoaster")
height = int(input("What is your height in cm:  "))

if height >= 120: # if height is greater than or equal 120cm
    print("You are tall enough to ride the rollercoaster")
else: # if height is less than 120cm
    print("You are too small to ride the rollercoaster")

